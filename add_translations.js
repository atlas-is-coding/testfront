// Функция для добавления переводов
function addTranslations() {
    // Регулярное выражение для поиска блоков переводов
    const translationPattern = /(\s+)([a-z_]+):\s*\{([^}]+)\}/g;
    
    // Находим все блоки переводов
    let match;
    const blocks = [];
    
    while ((match = translationPattern.exec(content)) !== null) {
        const key = match[2];
        const blockContent = match[3];
        const indent = match[1];
        
        // Проверяем, есть ли уже новые языки
        if (!blockContent.includes('ru:') && !blockContent.includes('de:')) {
            blocks.push({ key, blockContent, indent, fullMatch: match[0] });
        }
    }
    
    console.log(`Found ${blocks.length} translation blocks to update`);
    
    // Для каждого блока добавляем переводы
    blocks.forEach(block => {
        // Находим последний язык (обычно vi)
        const lastLangMatch = block.blockContent.match(/(\s+)(vi|tr|ja|es|ko|fr|zh|en):\s*`([^`]+)`/);
        if (lastLangMatch) {
            const lastLangIndent = lastLangMatch[1];
            const lastLang = lastLangMatch[2];
            const lastLangText = lastLangMatch[3];
            
            // Добавляем новые языки после последнего
            const newTranslations = `
            ru: \`${getTranslation('ru', lastLangText)}\`,
            de: \`${getTranslation('de', lastLangText)}\`,
            pt: \`${getTranslation('pt', lastLangText)}\`,
            it: \`${getTranslation('it', lastLangText)}\`,
            ar: \`${getTranslation('ar', lastLangText)}\`,
            hi: \`${getTranslation('hi', lastLangText)}\`,
            pl: \`${getTranslation('pl', lastLangText)}\`,
            nl: \`${getTranslation('nl', lastLangText)}\``;
            
            // Заменяем блок
            const newBlock = block.blockContent.replace(
                new RegExp(`(${lastLangIndent})(${lastLang}):\\s*\`([^\`]+)\``),
                `$1$2: \`$3\`,${newTranslations}`
            );
            
            content = content.replace(block.fullMatch, `${block.indent}${block.key}: {${newBlock}${block.indent}}`);
        }
    });
    
    // Сохраняем файл
    fs.writeFileSync(filePath, content, 'utf8');
    console.log('Translations added successfully!');
}

function getTranslation(lang, text) {
    // Простая функция для получения перевода
    // В реальности здесь должна быть логика перевода
    if (newTranslations[lang] && newTranslations[lang][text]) {
        return newTranslations[lang][text];
    }
    return text; // Возвращаем оригинал, если перевода нет
}

